    private void addVeterinarios() {

        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        Query q = em.createNamedQuery("Funcionario.findAll");
        jComboBox1.removeAllItems();
        for (Object d : q.getResultList()) {
            if (((Funcionario) d).getCodtipofuncionario().getCodtipofuncionario().intValue() == 2) {
                jComboBox1.addItem(((Funcionario) d).getNome());
            }
        }

    }
    
    private void setCons(BigDecimal idCons) {
        
        
        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        Query q = em.createNamedQuery("Consulta.findAll");
        Consulta con = null;
        for (Object d : q.getResultList()) {
            BigDecimal id = ((Consulta) d).getIdconsulta();
            if (id.compareTo(idCons) == 0) {
                con = ((Consulta) d);
            }
        }
        this.cons = con;
    }

    private void popularForm() {

        Preco.setText(this.cons.getPrecobase());
        data.setText(this.cons.getDatahoraconsulta());
        idan.setText(this.cons.getIdanimal().getNumchip().toString());

        this.cons.getIdfuncionario();

        ComboBoxModel model = jComboBox1.getModel();
        int tamanho = model.getSize();
        int index = 0;
        for (int i = 0; i < tamanho; i++) {
            String nome = (String) model.getElementAt(i);
            if(nome.equals(this.cons.getIdfuncionario().getNome())){
                index = i;
            }
        }
        
        jComboBox1.setSelectedIndex(index);

    }

    private Animal findAnimal(BigInteger numChip) {

        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        Query q = em.createNamedQuery("Animal.findAll");
        Animal animal = null;
        for (Object d : q.getResultList()) {
            if (((Animal) d).getNumchip().compareTo(numChip) == 0) {
                animal = (Animal) d;
            }
        }

        return animal;

    }


