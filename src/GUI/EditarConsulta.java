    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        int input = JOptionPane.showOptionDialog(null, "Pretende guardar as alterações efetuadas?", "Sucesso", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);

        if (input == JOptionPane.OK_OPTION) {
            this.cons.setPrecobase(Preco.getText());
            this.cons.setDatahoraconsulta(data.getText());
            Animal animal = findAnimal(new BigInteger(idan.getText()));
            this.cons.setIdanimal(animal);

            Funcionario funcionario = null;
            Query q = em.createNamedQuery("Funcionario.findAll");
            for (Object d : q.getResultList()) {
                if ((((Funcionario) d).getNome()).equals((String) jComboBox1.getSelectedItem())) {
                    funcionario = (Funcionario) d;
                }
            }
            this.cons.setIdfuncionario(funcionario);

            em.getTransaction().begin();
            em.merge(this.cons);
            em.getTransaction().commit();

            this.parentFrame.trocaPainel(new GestaoConsultas(this.parentFrame));
        }

    }//GEN-LAST:event_jButton1ActionPerformed

