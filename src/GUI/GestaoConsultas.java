    private void atualizarTabela() {
        DefaultTableModel model;
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        int idFunc = -1;
        Query q = em.createNamedQuery("Funcionario.findAll");
        for (Object d : q.getResultList()) {
            if ((((Funcionario) d).getNome()).equals((String) jComboBox1.getSelectedItem())) {
                idFunc = ((Funcionario) d).getIdfuncionario().intValue();
            }
        }

        q = em.createNamedQuery("Consulta.findAll");
        for (Object d : q.getResultList()) {
            if (((Consulta) d).getIdfuncionario().getIdfuncionario().intValue() == idFunc) {
                if (((Consulta) d).getEstado().equals("0")) {
                    String id = ((Consulta) d).getIdconsulta().toString();
                    String PrecoBase = ((Consulta) d).getPrecobase();
                    String Data = ((Consulta) d).getDatahoraconsulta();

                    model.addRow(new Object[]{id, PrecoBase, Data});
                }
            }
        }
    }


