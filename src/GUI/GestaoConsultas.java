    private void addVeterinarios() {

        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        Query q = em.createNamedQuery("Funcionario.findAll");
        jComboBox1.removeAllItems();
        for (Object d : q.getResultList()) {
            if (((Funcionario) d).getCodtipofuncionario().getCodtipofuncionario().intValue() == 2) {
                jComboBox1.addItem(((Funcionario) d).getNome());
            }
        }

    }

    private void apagaConsulta(int id) {

        EntityManager em = factory.createEntityManager();
        em.getTransaction().begin();

        Consulta consulta = new Consulta();

        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        Query q = em.createNamedQuery("Consulta.findAll");
        for (Object d : q.getResultList()) {
            if (id == ((Consulta) d).getIdconsulta().intValue()) {
                consulta = (Consulta) d;
            }
        }

        try {
            em.remove(consulta);
            em.flush();

        } catch (Exception e) {

            em.getTransaction().rollback();
        } finally {
            em.getTransaction().commit();
        }

        this.atualizarTabela();
    }
