    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nordemInput = nordem.getText();
        String nchipInput = nchip.getText();
        String desInput = des.getText();
        String precoInput = preco.getText();
        String dataInput = data.getText();
        boolean camposVazios = false;

        if (nordemInput.isEmpty() || dataInput.isEmpty() || nchipInput.isEmpty() || desInput.isEmpty() || precoInput.isEmpty()) {
            camposVazios = true;
        }

        if (camposVazios) {
            JOptionPane.showMessageDialog(null, "Preencher todos os campos obrigatórios!!!");

        } else {
            int input = JOptionPane.showOptionDialog(null, "Pretende marcar exame?", "Sucesso", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);

            if (input == JOptionPane.OK_OPTION) {
                factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
                EntityManager em = factory.createEntityManager();

                Exame exame = new Exame();
                Animal animal = findAnimal(new BigInteger(nchipInput));
                exame.setIdanimal(animal);
                exame.setIdfuncionario("" + findNordem(nordemInput));
                exame.setPrecoexame(precoInput);
                exame.setDescricaoexame(desInput);
                exame.setData(dataInput);
                exame.setEstado("0");

                em.getTransaction().begin();
                em.persist(exame);
                em.getTransaction().commit();

                this.parentFrame.trocaPainel(new AreaConsulta(this.parentFrame, idConsulta));

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

