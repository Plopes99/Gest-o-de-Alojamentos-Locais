    private void popularCampos() {
        factory = Persistence.createEntityManagerFactory(Persistence_UNIT_NAME);
        EntityManager em = factory.createEntityManager();
        
        Consulta consulta = em.find(Consulta.class, idConsulta);
        Animal animal = consulta.getIdanimal();
        Funcionario funcionario = consulta.getIdfuncionario();
        
        nordem.setText("" + funcionario.getNumregisto());
        nchip.setText("" + animal.getNumchip());
        
    }
